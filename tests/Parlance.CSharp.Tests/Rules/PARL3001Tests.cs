    /// <summary>
    /// Non-static local functions fold into their containing declaration's
    /// score. Reporting them as independent analysis targets would double-count
    /// their body (once in the parent's score, once on their own) — so the
    /// analyzer must emit a single diagnostic on the parent, not one on the
    /// parent and another on the local. This guards the scoping split
    /// documented in the PARL3001 contract against regressions where the
    /// LocalFunctionStatement registration forgets to skip non-static locals.
    /// </summary>
    [Fact]
    public async Task NonStaticLocalFunction_IsFoldedIntoParent_NotReportedIndependently()
    {
        // 20 sequential `if`s in the local: standalone score 20 (would fire
        // PARL3001 on its own under the buggy dual-report behavior). Folded
        // into the parent at nesting 1 each `if` scores +2 → parent's score
        // is 40, comfortably over the 15 threshold. The fix makes this method
        // produce exactly one diagnostic, on M.
        const string source = """
            class C
            {
                void M(bool b)
                {
                    void Local()
                    {
                        if (b) { }
                        if (b) { }
                        if (b) { }
                        if (b) { }
                        if (b) { }
                        if (b) { }
                        if (b) { }
                        if (b) { }
                        if (b) { }
                        if (b) { }
                        if (b) { }
                        if (b) { }
                        if (b) { }
                        if (b) { }
                        if (b) { }
                        if (b) { }
                        if (b) { }
                        if (b) { }
                        if (b) { }
                        if (b) { }
                    }

                    Local();
                }
            }
            """;

        var diagnostics = await RunAnalyzerAllAsync(source);

        var diagnostic = Assert.Single(diagnostics);
        Assert.Contains("'M'", diagnostic.GetMessage());
    }

    /// <summary>
    /// Static local functions are scored independently: the parent's score
    /// excludes the static body, and the diagnostic (if any) fires on the
    /// local itself. This test sits opposite the non-static case above so the
    /// two scoping rules are pinned down by analyzer-level coverage, not just
    /// by the metric's unit tests.
    /// </summary>
    [Fact]
    public async Task StaticLocalFunction_ReportsItself_ParentStaysSilent()
    {
        // Same 20-if body, but `static`. Parent's score excludes the body
        // entirely, so M scores 0. The static local's standalone score is 20,
        // over the 15 threshold — exactly one diagnostic, on Local.
        const string source = """
            class C
            {
                void M(bool b)
                {
                    static void Local(bool b)
                    {
                        if (b) { }
                        if (b) { }
                        if (b) { }
                        if (b) { }
                        if (b) { }
                        if (b) { }
                        if (b) { }
                        if (b) { }
                        if (b) { }
                        if (b) { }
                        if (b) { }
                        if (b) { }
                        if (b) { }
                        if (b) { }
                        if (b) { }
                        if (b) { }
                        if (b) { }
                        if (b) { }
                        if (b) { }
                        if (b) { }
                    }

                    Local(b);
                }
            }
            """;

        var diagnostics = await RunAnalyzerAllAsync(source);

        var diagnostic = Assert.Single(diagnostics);
        Assert.Contains("'Local'", diagnostic.GetMessage());
    }

    private static async Task<Diagnostic> RunAnalyzerAsync(string source)
    {
        var diagnostics = await RunAnalyzerAllAsync(source);
        return diagnostics.Single();
    }

    private static async Task<ImmutableArray<Diagnostic>> RunAnalyzerAllAsync(string source)
    {
        var tree = CSharpSyntaxTree.ParseText(source);
        var compilation = CSharpCompilation.Create(
            "EncodingTest",
            [tree],
            [MetadataReference.CreateFromFile(typeof(object).Assembly.Location)],
            new CSharpCompilationOptions(OutputKind.DynamicallyLinkedLibrary));

        var analyzers = ImmutableArray.Create<DiagnosticAnalyzer>(new PARL3001_CognitiveComplexityThreshold());
        var withAnalyzers = compilation.WithAnalyzers(analyzers);
        var diagnostics = await withAnalyzers.GetAnalyzerDiagnosticsAsync();

        return [.. diagnostics.Where(d => d.Id == PARL3001_CognitiveComplexityThreshold.DiagnosticId)];
    }
