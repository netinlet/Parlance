    /// <summary>
    /// PARL3001 encodes each cognitive-complexity increment into the diagnostic
    /// as a pair: the N-th <see cref="Diagnostic.AdditionalLocations"/> entry
    /// and the <c>increment.N.reason</c> property. A consumer decoding these
    /// two channels in parallel must reconstruct every increment's location
    /// and reason. This test exercises all three reason-string variants the
    /// metric emits — <c>"+1"</c>, <c>"+N (incl M for nesting)"</c>, and
    /// <c>"+1 (recursion)"</c> — on a single method so the wire format is
    /// locked down.
    /// </summary>
    [Fact]
    public async Task EncodesIncrementsAsAdditionalLocationsAndProperties()
    {
        // Crafted to exceed the default threshold (15) while hitting every
        // reason-string variant the metric emits:
        //   if @ nesting 0  → "+1"
        //   if @ nesting 1..4 → "+N (incl M for nesting)"
        //   recursive M(b) → "+1 (recursion)"
        // Score: 1 + 2 + 3 + 4 + 5 + 1 + 1 + 1 = 18.
        const string source = """
            class C
            {
                int M(bool b)
                {
                    if (b)
                    {
                        if (b)
                        {
                            if (b)
                            {
                                if (b)
                                {
                                    if (b)
                                    {
                                        return M(b);
                                    }
                                }
                            }
                        }
                    }
                    if (b) { }
                    if (b) { }
                    if (b) { }
                    return 0;
                }
            }
            """;

        var diagnostic = await RunAnalyzerAsync(source);

        var countRaw = diagnostic.Properties["increment.count"];
        Assert.NotNull(countRaw);
        var count = int.Parse(countRaw!, System.Globalization.CultureInfo.InvariantCulture);

        Assert.Equal(count, diagnostic.AdditionalLocations.Count);
        Assert.True(count >= 3, $"expected at least 3 increments, got {count}");

        var reasons = Enumerable.Range(0, count)
            .Select(i => diagnostic.Properties[$"increment.{i}.reason"])
            .ToList();

        Assert.Contains("+1", reasons);
        Assert.Contains(reasons, r => r is not null && r.Contains("incl ") && r.Contains(" for nesting"));
        Assert.Contains("+1 (recursion)", reasons);

        foreach (var location in diagnostic.AdditionalLocations)
        {
            Assert.True(location.IsInSource);
            Assert.True(location.SourceSpan.Length >= 0);
        }
    }

    /// <summary>
    /// The property threshold (default 3) is intentionally much smaller than
    /// the method threshold (default 15). A body that would be silent on a
    /// method must fire PARL3001 when embedded in a property arrow body,
    /// indexer arrow body, or any accessor — the whole point of the split.
    /// </summary>
    [Theory]
    [InlineData("PropertyArrow", """
        class C
        {
            private bool _a, _b, _c, _d;
            public int P => (_a && _b) || (_c && _d) ? 1 : 0;
        }
        """, "P")]
    [InlineData("IndexerArrow", """
        class C
        {
            private bool _a, _b, _c, _d;
            public int this[int i] => (_a && _b) || (_c && _d) ? 1 : 0;
        }
        """, "this[]")]
    [InlineData("GetAccessor", """
        class C
        {
            private bool _a, _b, _c, _d;
            public int P
            {
                get
                {
                    if (_a && _b) return 1;
                    if (_c && _d) return 2;
                    return 0;
                }
            }
        }
        """, "P.get")]
    [InlineData("SetAccessor", """
        class C
        {
            private bool _a, _b, _c, _d;
            private int _p;
            public int P
            {
                get => _p;
                set
                {
                    if (_a && _b) _p = value;
                    else if (_c && _d) _p = -value;
                    else _p = 0;
                }
            }
        }
        """, "P.set")]
    [InlineData("InitAccessor", """
        class C
        {
            private bool _a, _b, _c, _d;
            private int _p;
            public int P
            {
                get => _p;
                init
                {
                    if (_a && _b) _p = value;
                    else if (_c && _d) _p = -value;
                    else _p = 0;
                }
            }
        }
        """, "P.init")]
    [InlineData("AddAccessor", """
        using System;
        class C
        {
            private bool _a, _b, _c, _d;
            private EventHandler _h;
            public event EventHandler E
            {
                add
                {
                    if (_a && _b) _h += value;
                    else if (_c && _d) _h = value;
                    else _h = null;
                }
                remove => _h -= value;
            }
        }
        """, "E.add")]
    [InlineData("RemoveAccessor", """
        using System;
        class C
        {
            private bool _a, _b, _c, _d;
            private EventHandler _h;
            public event EventHandler E
            {
                add => _h += value;
                remove
                {
                    if (_a && _b) _h -= value;
                    else if (_c && _d) _h = null;
                    else _h = null;
                }
            }
        }
        """, "E.remove")]
    public async Task Reports_WhenPropertyShapedDeclarationExceedsPropertyThreshold(
        string scenario, string source, string expectedName)
    {
        _ = scenario;

        var diagnostic = await RunAnalyzerAsync(source);

        Assert.Equal(PARL3001_CognitiveComplexityThreshold.DiagnosticId, diagnostic.Id);
        Assert.Equal(expectedName, diagnostic.GetMessage()!.Split('\'')[1]);
    }

