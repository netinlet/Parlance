    [Fact]
    public async Task Reports_WhenComplexityExceedsThreshold()
    {
        var source = """
            class C
            {
                void {|#0:M|}()
                {
                MyLabel:
                    for (var i = 0; i < 10; i++)
                    {
                        foreach (var item in "")
                        {
                            while (true)
                            {
                                do
                                {
                                    if (true)
                                    {
                                        goto MyLabel;
                                    }
                                } while (false);
                            }
                        }
                    }
                }
            }
            """;

        // Score breakdown under Parlance's hybrid goto rule (T4.5):
        //   for (+1) + foreach (+2) + while (+3) + do (+4) + if (+5)
        //   + goto at nesting 5 (+6) = 21.
        var expected = Verify.Diagnostic("PARL3001")
            .WithLocation(0)
            .WithSeverity(Microsoft.CodeAnalysis.DiagnosticSeverity.Warning)
            .WithOptions(DiagnosticOptions.IgnoreAdditionalLocations)
            .WithArguments("M", 21, 15);

        await Verify.VerifyAnalyzerAsync(source, expected);
    }

